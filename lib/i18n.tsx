const DICTS: Record<Lang, Dict> = { en, ru };
const STORAGE_KEY = "wearit:lang";

interface I18nValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Dict;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  // Always start "en" so server and first client render match (no hydration mismatch).
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    let initial: Lang | null = null;
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved === "en" || saved === "ru") initial = saved;
    } catch { /* ignore */ }
    if (!initial && typeof navigator !== "undefined" && navigator.language?.toLowerCase().startsWith("ru")) {
      initial = "ru";
    }
    if (initial && initial !== "en") setLangState(initial);
  }, []);

  useEffect(() => {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
    if (typeof document !== "undefined") document.documentElement.lang = lang;
  }, [lang]);

  const value: I18nValue = { lang, setLang: setLangState, t: DICTS[lang] };
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    // Safe fallback if used outside the provider — defaults to English.
    return { lang: "en", setLang: () => {}, t: en };
  }
  return ctx;
}

/* Compact EN / RU toggle. `variant` adjusts colours for dark vs light surfaces. */
export function LangSwitch({ variant = "light", style }: { variant?: "light" | "dark"; style?: React.CSSProperties }) {
  const { lang, setLang } = useI18n();
  const isDark = variant === "dark";
  const langs: Lang[] = ["en", "ru"];
  return (
    <div
      role="group"
      aria-label="Language"
      style={{
        display: "inline-flex",
        alignItems: "center",
        gap: 2,
        padding: 2,
        borderRadius: 100,
        border: `1px solid ${isDark ? "rgba(255,255,255,0.16)" : "var(--border)"}`,
        background: isDark ? "rgba(255,255,255,0.06)" : "var(--card)",
        ...style,
      }}
    >
      {langs.map((l) => {
        const on = lang === l;
        return (
          <button
            key={l}
            onClick={() => setLang(l)}
            aria-pressed={on}
            style={{
              border: "none",
              cursor: "pointer",
              borderRadius: 100,
              padding: "4px 10px",
              fontSize: 12,
              fontWeight: 600,
              letterSpacing: "0.02em",
              fontFamily: "'Hanken Grotesk',sans-serif",
              transition: "background .15s, color .15s",
              background: on ? (isDark ? "#fff" : "var(--ink)") : "transparent",
              color: on ? (isDark ? "#111" : "#fff") : (isDark ? "rgba(255,255,255,0.7)" : "var(--muted)"),
            }}
          >
            {l.toUpperCase()}
          </button>
        );
      })}
    </div>
  );
}
